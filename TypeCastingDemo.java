
public class TypeCastingDemo {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		double a = 99.9;
		int b =(int)a;
		byte c =(byte)a;
		short d =(short)a;
		float e =(float)a;
		System.out.println("double to integer= " +b);
		System.out.println("double to byte= " +c);
		System.out.println("double to short= " +d);
		System.out.println("double to float= " +e);
	}

}
